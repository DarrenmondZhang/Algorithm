// @lc code=start
// 最近相关性 用栈解决
// 1. 暴力求解 不断replace匹配的括号， 替换成空字符串
// 2. 栈 
class Solution {
    public boolean isValid(String s) {
        Stack<Character> stack = new Stack<Character>();
        // 遍历整个字符串
        for (char c : s.toCharArray()) {
            if (c == '(')
                stack.push(')');
            else if (c == '{')
                stack.push('}');
            else if (c == '[')
                stack.push(']');
            else if (stack.isEmpty() || stack.pop() != c)
                // pop 出来的值和当前扫描到的值是否一样
                return false;
         }
         return stack.isEmpty();
    }
}
